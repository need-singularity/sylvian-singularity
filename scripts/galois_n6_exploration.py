def necklace_count(n, q):
    """Number of irreducible polys of degree n over F_q = (1/n) sum_{d|n} mu(n/d)*q^d"""
    total = 0
    for d in divisors(n):
        mu_val = sympy.mobius(n // d)
        total += mu_val * (q ** d)
    return total // n

